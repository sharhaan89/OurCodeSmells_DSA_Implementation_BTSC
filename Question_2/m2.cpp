#include <iostream>
#include <vector>
#include <string>
#include <cstring>
using namespace std;
class Flight{
    public:
        // initializes flight with rows and columns
        Flight(int rows){
            initializeflight(rows);
        }

        // display the seats after operations
        void displayseats() const{
            for(const auto & rows: seat_layout){
                for(int i=0; i<rows.size(); i++){
                    if(i==3){
                        cout<<"|  |  ";
                        continue;
                    }
                    cout<<rows[i]<<"  ";
                }
                cout<<endl;
            }
        }

        // index (number of rows, number of columns)
        bool reserveseat(int row, int col){
            if(isFlightFull()) return false;
            if (row >= 0 && row < seat_layout.size() && col>= 0 && col< seat_layout[0].size() && col!=3) {
                if (seat_layout[row][col] == "available") {
                    seat_layout[row][col] = "reserved ";
                    return true;
                }
            }
            return false;
        }

        // index (number of rows, number of columns)
        bool cancelreservation(int row, int col){
            if (row >= 0 && row < seat_layout.size() && col>= 0 && col< seat_layout[0].size() && col!=3) {
                if (seat_layout[row][col] == "reserved ") {
                    seat_layout[row][col] = "available";
                    return true;
                }
            }
            return false;
        }

        // (seat preference, number of rows)
        void checkseatavailability(string seat_pref,int rows){
            int st=-1,ed=-1;
            for(char& x: seat_pref) tolower(x);
            if(seat_pref=="window"){
                st=0, ed=6;
            }
            if(seat_pref=="aisle"){
                st=2, ed=4;
            }
            if(seat_pref=="middle"){
                st=1, ed=5;
            }
            if(st==ed && st==-1){
                cout<<"wrong input"<<endl;
                return;
            }
            int c=0;
            for(int j=0; j<rows; j++){
                for(int i=0; i<7; i++){
                    if(seat_layout[j][i]=="available" && (i==st || i==ed)){
                        cout<<"seat available at row "<<j<<" and column "<<i<<endl; 
                        c=1;
                    }
                }
            }
            if(!c) cout<<" no seat available "<<endl; 
        }

        // (seat preference, number of rows)
        void suggestoptimalseat(string seat_pref,int rows){
            int st=-1,ed=-1;
            for(char& x: seat_pref) tolower(x);
            if(seat_pref=="window"){
                st=0, ed=6;
            }
            if(seat_pref=="aisle"){
                st=2, ed=4;
            }
            if(seat_pref=="middle"){
                st=1, ed=5;
            }
            if(st==ed && st==-1){
                cout<<"wrong input"<<endl;
                return;
            }
            for(int j=0; j<rows; j++){
                for(int i=0; i<7; i++){
                    if(seat_layout[j][i]=="available" && (i==st || i==ed)){
                        cout<<"seat available at row "<<j<<" and column "<<i<<endl; 
                        return;
                    }
                }
            }
            cout<<" no seat available "<<endl; 
        }

        // new reservation(m,n) and old reservation(x,y)
        bool changereservation(int x,int y, int m, int n){
            if(seat_layout[x][y]=="reserved "){
                if (m >= 0 && m < seat_layout.size() && n>= 0 && n< seat_layout[0].size() && n!=3) {
                    if (seat_layout[m][n] == "available") {
                        seat_layout[m][n] = "reserved ";
                        seat_layout[x][y] = "available";
                        return true;
                    }
                }
                cout<<"seat already reserved"<<endl;
                return false;
            }
            return false;
        }

        //(number of rows, number of columns, waiting list numbers)
        void waitingList(int & wl){
            if(!isFlightFull()){
                int inp;
                cout<<"apply for waiting list(0/1) ? \n";
                cin>>inp;
                if(inp){
                    wl++;
                    cout<<"current waiting list: "<<wl<<endl;
                    return;
                }
            }
        }
    private:
        vector<vector<string>> seat_layout;

        void initializeflight(int rows){
            seat_layout.resize(rows, vector<string>(7, "available"));
        }
        bool isFlightFull() const {
            for (const auto& row : seat_layout) {
                for (const auto& seat : row) {
                    if (seat == "available") {
                        return false;
                    }
                }
            }
            return true;
        }
};
int main(){
    // index at 3 represents passage
    Flight SkyWings(7);
    SkyWings.displayseats();
    for(int i=0;i<7; i++){
        for(int j=0; j<7; j++){
            SkyWings.reserveseat(i,j);
        }
    }
    cout<<"\n\n\n";
    SkyWings.displayseats();
    int wl=0;
    SkyWings.waitingList(wl);
}